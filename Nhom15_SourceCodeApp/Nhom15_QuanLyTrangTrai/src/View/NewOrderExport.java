    private void jComboBoxMaKMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxMaKMActionPerformed
        // TODO add your handling code here:
        int index = jComboBoxMaKM.getSelectedIndex();
        codeDiscount = arrLDisCode.get(index);
        
        double triGia = 0;
        try {
            triGia = C_NewOrderExport.timGiaTriKhuyenMai(codeDiscount);
            jLabelValue.setText(String.valueOf(triGia * 100) + "%");
        } catch (SQLException ex) {
            Logger.getLogger(NewOrderExport.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NewOrderExport.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        // Hien thi thanh tien sau cung khi chon ma khuyen mai
        double thanhTien = totalOrder * (1 -  triGia);
        jLabelThanhTien.setText(String.valueOf(thanhTien));
    }//GEN-LAST:event_jComboBoxMaKMActionPerformed

