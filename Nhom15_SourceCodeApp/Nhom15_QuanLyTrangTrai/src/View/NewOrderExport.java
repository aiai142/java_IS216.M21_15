    private void jButtonThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonThanhToanActionPerformed
        String disID;
        String diaChiNhan = txtFDiaChi.getText();
        
        if (tonTai) { // Neu khach hang da co trong he thong
            try {
                // them hoa don vao co so du lieu
                disID = C_NewOrderExport.layDisID(codeDiscount);
                
                double preTotal = 0;
                double total = 0;

                Order_Export newOrder = new Order_Export(dateOrder, info_kh[0], diaChiNhan, preTotal, disID, total, payBy, shipCode);

                // them hoa don moi vao co so du lieu
                String maHD = C_NewOrderExport.themOrderExport(newOrder);

                // lay ra danh sach san pham duoc chon mua
                ArrayList<Product> arrLProPurchase = C_NewOrderExport.arrLProPurchase;
                
                // them du lieu tu bang chon mua san pham vao bang CTHD
                for (int v = 0; v < arrLProPurchase.size(); v++) {
                    String tenSP = arrLProPurchase.get(v).getProName();
                    String maSP;
                    try {
                        maSP = C_Product.getProID(tenSP);
                        int soLuong = arrLProPurchase.get(v).getQuantity();

                        Order_Details_Ex newCTHD = new Order_Details_Ex(maHD, maSP, soLuong);
                        C_Order_Details_Ex.themCTHD(newCTHD);
                        
                    } catch (ClassNotFoundException ex) {
                        Logger.getLogger(NewOrderExport.class.getName()).log(Level.SEVERE, null, ex);
                    }

                }

                //in thong bao them thanh cong
                JOptionPane.showMessageDialog(this, "Đơn hàng đã được ghi nhận");
                this.setVisible(false);
                new AdminHome(id_login).setVisible(true);
                
            } catch (SQLException ex) {
                Logger.getLogger(NewOrderExport.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(NewOrderExport.class.getName()).log(Level.SEVERE, null, ex);
            }
        } 
        else {  // Them khach hang moi
            try {
                disID = null;
                double preTotal = 0;
                double total = 0;
                
                //kiem tra thong tin ho ten va so dien thoai khach hang khong duoc de trong
                if (txtFTenKH.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Nhập họ tên và số điện thoại khách hàng là bắt buộc!");
                } 
                
                else {
                    // them moi khach vang lai vao DB va tra ve maKH
                    String maKH = C_NewOrderExport.layMaKH(txtFTenKH.getText(), txtFSdtKH.getText());

                    Order_Export newOrder = new Order_Export(dateOrder, maKH, diaChiNhan, preTotal, disID, total, payBy, shipCode);

                    // them hoa don moi vao co so du lieu
                    String maHD = C_NewOrderExport.themOrderExport(newOrder);

                    // lay ra danh sach san pham duoc chon mua
                    ArrayList<Product> arrLProPurchase = C_NewOrderExport.arrLProPurchase;

                    // them danh sach chon mua vao bang CTHD
                    for (int v = 0; v < arrLProPurchase.size(); v++) {
                        String tenSP = arrLProPurchase.get(v).getProName();
                        String maSP;
                        try {
                            maSP = C_Product.getProID(tenSP);
                            int soLuong = arrLProPurchase.get(v).getQuantity();

                            Order_Details_Ex newCTHD = new Order_Details_Ex(maHD, maSP, soLuong);
                            C_Order_Details_Ex.themCTHD(newCTHD);
                            
                        } catch (ClassNotFoundException ex) {
                            Logger.getLogger(NewOrderExport.class.getName()).log(Level.SEVERE, null, ex);
                        }

                    }
                    
                       //in thong bao them thanh cong
                    JOptionPane.showMessageDialog(this, "Đơn hàng đã được ghi nhận");
                    this.setVisible(false);
                    new AdminHome(id_login).setVisible(true);
                }

            } catch (SQLException ex) {
                Logger.getLogger(NewOrderExport.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(NewOrderExport.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButtonThanhToanActionPerformed

