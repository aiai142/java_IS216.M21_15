package Model;

/**
 *
 * @author Lenovo
 */
public class Statistical_month {
    private long total;
    private String month;

    public Statistical_month() {
    }

    public Statistical_month(long total, String month) {
        this.total = total;
        this.month = month;
    }

    public long getTotal() {
        return total;
    }

    public void setTotal(long total) {
        this.total = total;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    
}
