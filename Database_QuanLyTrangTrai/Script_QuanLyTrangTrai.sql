        UPDATE ORDER_EXPORT 
        SET preTotal = pre_total + (:NEW.num_Products * pro_price),
            orderTotal = total + (:NEW.num_Products * pro_price)
        WHERE ord_Ex_Num = :NEW.ord_Ex_Num;  
