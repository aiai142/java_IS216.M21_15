    SELECT c.CusID, c.accrued_money, c.userID, OrderTotal INTO cus_id, money_of_cus, userNum, value_ord
    FROM ORDER_EXPORT oe JOIN CUSTOMER c ON oe.CusID = c.CusID
    WHERE TransID = :NEW.TransID;

