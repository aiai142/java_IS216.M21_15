DECLARE
    pro_price PRODUCT.ProPrice%TYPE;
    pre_total ORDER_EXPORT.PreTotal%TYPE;
    total ORDER_EXPORT.orderTotal%TYPE;
    dis_value ORDER_EXPORT.disID%TYPE;
